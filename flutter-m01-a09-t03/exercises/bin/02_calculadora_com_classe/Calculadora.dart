class Calculadora {
  num somar(num valor1, num valor2) {
    return valor1 + valor2;
  }

  num subtrair(num valor1, num valor2) {
    return valor1 - valor2;
  }

  num dividir(num valor1, num valor2) {
    return valor1 / valor2;
  }

  num multiplicar(num valor1, num valor2) {
    return valor1 * valor2;
  }
}
